int		ft_calc(int a, char op, int b)
{
	if (op == '*')
		return (a * b);
	if (op == '+')
		return (a + b);
	if (op == '-')
		return (a - b);
	if (op == '/')
		return (a / b);
	if (op == '%')
		return (a % b);
	return (0);
}
